package schoolManagementApp;

class Student {
	String name;
	String DOB;
	String gender;
	String phoneNumber;
	String city;
	Student(String name,String DOB,String gender,String phoneNumber, String city){
		this.name = name;
		this.DOB = DOB;
		this.gender = gender;
		this.phoneNumber = phoneNumber;
		this.city = city;
	}

}
